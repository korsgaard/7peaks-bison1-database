CREATE VIEW [dbo].[LikeCountDate]
AS
SELECT        DATEADD(dd, DATEDIFF(dd, 0, dbo.NxtFeedItemLike.CreateTS), 0) AS Date, COUNT(dbo.NxtFeedItemLike.FeedItemLikeID) AS Likes, dbo.NxtFeedItemLike.UserFK
FROM            dbo.NxtFeedItemLike INNER JOIN
                         dbo.NxtFeedItem ON dbo.NxtFeedItemLike.FeedItemFK = dbo.NxtFeedItem.FeedItemID
GROUP BY DATEADD(dd, DATEDIFF(dd, 0, dbo.NxtFeedItemLike.CreateTS), 0), dbo.NxtFeedItemLike.UserFK
GO

